class KeybindList
  def initialize
    @keybinds = InputCode.read_keybinds
  end

  def key(input)
    keybind = nil
    for k in @keybinds
      if k.target == input
        if k.src_type == 1
          if !keybind || k.input_code < keybind.input_code
            keybind = k
          end
        end
      end
    end
    if keybind
      InputCode.getName(keybind.input_code)
    end
    return "Unknown Input"
  end

  def rect(input)
    keybind = nil
    for k in @keybinds
      if k.target == input
        if k.src_type == 1
          if !keybind || k.input_code < keybind.input_code
            keybind = k
          end
        end
      end
    end
    if keybind
      id = keybind.input_code
      return Rect.new((id%16)*28,(id/16).floor*28,28,28)
    end
    return Rect.new(0, 0, 28, 28)
  end
end

